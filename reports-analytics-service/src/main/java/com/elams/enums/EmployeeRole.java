package com.elams.enums;

/**
 * Enumeration representing the roles of an employee.
 * This enum defines the available employee roles, such as MANAGER and EMPLOYEE.
 */
public enum EmployeeRole {

    /**
     * Represents an employee with managerial responsibilities.
     */
    MANAGER,

    /**
     * Represents a regular employee without managerial responsibilities.
     */
    EMPLOYEE
}