package com.elams.entities;

/**
 * Enumeration representing different types of leave.
 * This enum defines the available leave types, such as SICK, VACATION, CASUAL, and OTHER.
 */
public enum LeaveType {

    /**
     * Leave taken due to sickness.
     */
    SICK,

    /**
     * Leave taken for vacation or personal time off.
     */
    VACATION,

    /**
     * Casual leave taken for miscellaneous reasons.
     */
    CASUAL,

    /**
     * Other types of leave not covered by the above categories.
     */
    OTHER
}